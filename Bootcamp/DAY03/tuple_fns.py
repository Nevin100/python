#1.)to check the occurence of an element in a tuple...
tple1=(1,2,3,1,2,3,4,5,6,7,8,9)
print(tple1.count(1))

#2.)to know about the index of a tuple element...
tple2=(1,2,3,1,2,3,4,5,6,7,8,9)
print(tple2.index(3))
 
#3.)for max and min values of a given tuple...
tple3=(1,2,3,1,2,3,4,5,6,7,8,9)
print(max(tple3))
print(min(tple3))

#4.)for the length of a given tuple...
tple4=(1,2,3,1,2,3,4,5,6,7,8,9)
print(len(tple4))

#5.)deleting a complete tuple...
tple5=(1,2,3,1,2,3,4,5,6,7,8,9)
del tple5
